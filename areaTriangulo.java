
/*
    Criar uma aplicação que calcule a área de um triângulo.
    Sabendo que a base receberá o valor de 15  e a altura o valor de 7,5.
    
    Fórmula para calcular o triângulo: area = base * altura / 2:
*/


public class areaTriangulo {
    public static void main(String[] args) {
        
        double base = 15;
        double altura = 7.5;
        double area = (base * altura) / 2;

        System.out.print(" O valor da área do triangulo é: " + area);
    }
    
}
